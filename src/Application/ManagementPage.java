    private void expenceSearchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_expenceSearchKeyPressed
        try {
            DefaultTableModel model = (DefaultTableModel) expenceTable.getModel();
            String sql = "select * from expences where name like ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, expenceSearch.getText() + "%");
            ResultSet rs = pst.executeQuery();
            removeExpenceTableRow();
            while(rs.next()){
                int serial = rs.getInt("serial");
                String exName = rs.getString("name");
                String exDate = rs.getString("date");
                String exCategory = rs.getString("category");
                String exAccount = rs.getString("accnum");
                double exAmount = rs.getDouble("amount");
                String exDescription = rs.getString("description");
                Object[] row = {serial, exName, exDate, exCategory, exAccount, exAmount, exDescription};                
                model.addRow(row);
            }
             
        } catch (SQLException ex) {
            Logger.getLogger(ManagementPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_expenceSearchKeyPressed

