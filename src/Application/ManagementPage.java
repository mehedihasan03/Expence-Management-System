    private boolean userValidate() {
        boolean isValid = true;
        String msg = "";
        final String regNum = "[0-9]+([.][0-9]{1,2})?";
        if (tfUsername.getText().isEmpty()) {
            isValid = false;
            msg += "username cannot be empty \n";
        }
        if (!tfUsername.getText().matches("[a-zA-Z0-9]+")) {
            isValid = false;
            msg += "use a valid username \n";
        }
        if (tfContact.getText().isEmpty()) {
            isValid = false;
            msg += "Contact number cannot be empty \n";
        }
        if (tfContact.getText().length() != 11) {
            isValid = false;
            msg += "Contact number must contain 11 digit \n";
        }
        if (!tfContact.getText().matches("[0-9]+")) {
            isValid = false;
            msg += "contact number must be digit \n";
        }
        if (!tfEmail.getText().matches("[A-Za-z0-9._]+@[A-Za-z]+\\.[A-Za-z]{2,6}")) {
            isValid = false;
            msg += "Use a valid Email \n";
        }
        if (!male.isSelected() && !female.isSelected()) {
            isValid = false;
            msg += "Select your gender \n";
        }
        if (cbOcupation.getSelectedIndex() == 0) {
            isValid = false;
            msg += "Select your occupation \n";
        }
        if (tfPassword.getText().isEmpty()) {
            isValid = false;
            msg += "type a new password \n";
        }
        if (tfRePassword.getText().isEmpty()) {
            isValid = false;
            msg += "Re-type your password \n";
        }
        if (!tfPassword.getText().matches(tfRePassword.getText())) {
            isValid = false;
            msg += "Password not matched \n";
        }
        if (!isValid) {
            JOptionPane.showMessageDialog(rootPane, msg);
        }
        return isValid;
    }

