    private void StoreExpenceData() {
        if (expenceValidation()) {
            try {
                String exName = eName.getText();
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String exDate = sdf.format(eDate.getDate());
                String exCategory = (String) eCategory.getSelectedItem();
                String exAccount = eAccount.getText();
                String exAmount = eAmount.getText();
                String exDescription = eDescription.getText();
                if ((Double.parseDouble(exAmount)) < RemainingBallence()) {
                    String sql = "insert into expences "
                            + "(name, date, category, accnum, amount, description)"
                            + " values (?, ?, ?, ?, ?, ?);";
                    PreparedStatement pst = con.prepareStatement(sql);
                    pst.setString(1, exName);
                    pst.setString(2, exDate);
                    pst.setString(3, exCategory);
                    pst.setString(4, exAccount);
                    pst.setDouble(5, Double.parseDouble(exAmount));
                    pst.setString(6, exDescription);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(rootPane, "Your expence added successfully");
                    ResetExpenceForm();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Your current balance is not enough for this expence");
                }
            } catch (SQLException ex) {
                ex.getLocalizedMessage();
            }
        }
    }

